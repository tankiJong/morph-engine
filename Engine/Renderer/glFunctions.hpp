//------------------------------------------------------------------------
inline bool GLCheckError(char const *file, int line) {
#if defined(_DEBUG)
  GLenum error = glGetError();
  if (error != GL_NO_ERROR) {
    ERROR_RECOVERABLE(Stringf("GL ERROR [0x%04x] at [%s(%i)]", error, file, line));
    return true;
  }
#endif
  return false;
}

#define GL_CHECK_ERROR()  GLCheckError( __FILE__, __LINE__ )

//------------------------------------------------------------------------
inline bool GLFailed() {
  return GL_CHECK_ERROR();
}

//------------------------------------------------------------------------
inline bool GLSucceeded() {
  return !GLFailed();
}

